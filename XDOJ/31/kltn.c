#include<stdio.h>
#include<math.h>

int gcd(int a,int b) {
	if(a==1||b==1)
		return 1;
    if(b==0)
		return a;
	if(a%b==0)
		return b;
	//辗转相除
    return gcd(b,a%b);
}

int main(){
	int a,b;
	scanf("%d%d",&a,&b);
	if(a<b)
		printf("%d",gcd(b,a));
	else
		printf("%d",gcd(a,b));
	return 0;
}
